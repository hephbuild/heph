//! `go_compile` — the single driver that compiles a Go package archive.
//!
//! Replaces the former `sh`-scripted compile *and* the separate `go_embed`
//! target: importcfg generation, `//go:embed` resolution, the asm/symabis/pack
//! pipeline, and `go tool compile` all happen in one target. Folding embed into
//! the compile means embed assets are staged once (here), not also into a
//! separate `go_embed` sandbox — and the `go_embed_src` lane (assets kept out of
//! `_golist`) is resolved in-process against the staged files with the
//! Go-faithful selector in [`crate::plugingo::embed`].
//!
//! Mirrors the exact flag set the old scripts used (`-p`, `-trimpath`, `-pack`,
//! `-shared`, `-symabis`, `-asmhdr`, `-embedcfg`) so archives stay
//! byte-compatible.

