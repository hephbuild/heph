/// Parameters for [`build_compile_spec`]. Groups the (necessarily many) compile
/// inputs so each builder (`target_lib`, `target_test`, `thirdparty`) just fills
/// this in.
pub struct CompileParams<'a> {
    pub addr: Addr,
    /// `-p` flag: `main` for main packages, else the import path.
    pub p_flag: String,
    /// Output archive filename (package-relative).
    pub out_file: String,
    pub factors: &'a Factors,
    pub go_version: &'a str,
    /// `(import_path, lib archive addr)` — importcfg entries + `lib_*` dep groups.
    pub transitive_libs: &'a [(String, Addr)],
    /// Compile sources (`.go`) → the default (`""`) dep group.
    pub src_addrs: &'a [String],
    /// `.s` assembly source names (package-relative) driving the asm steps.
    pub s_files: &'a [String],
    /// Addrs that stage the `.s` sources into the sandbox.
    pub s_file_addrs: &'a [String],
    /// `.h` header addrs (next to / `#include`d by the `.s` sources).
    pub hdr_addrs: &'a [String],
    /// `golist|pkg` addr — read for `EmbedPatterns`/`EmbedFiles`. `Some` iff the
    /// package embeds.
    pub golist_addr: Option<&'a Addr>,
    /// `""` for no embed, else `embed` / `test_embed` / `xtest_embed`.
    pub embed_variant: &'a str,
    /// `go_src` embed file addrs (Go resolved) staged for the compile.
    pub embed_file_addrs: &'a [String],
    /// `go_embed_src` lane addrs (assets `go list` never saw), resolved in-driver.
    pub embed_src_addrs: &'a [String],
}

/// Build a `go_compile` target spec. Replaces the former `sh`-script compile and
/// the separate `go_embed` target.
pub fn build_compile_spec(p: CompileParams) -> TargetSpec {
    let mut deps: BTreeMap<String, Value> = p
        .transitive_libs
        .iter()
        .map(|(import_path, dep_addr)| {
            (
                import_path_to_dep_group(import_path),
                Value::List(vec![Value::String(dep_addr.format())]),
            )
        })
        .collect();

    let str_list = |v: &[String]| Value::List(v.iter().cloned().map(Value::String).collect());

    deps.insert(String::new(), str_list(p.src_addrs));
    if !p.s_file_addrs.is_empty() {
        deps.insert("asm".to_string(), str_list(p.s_file_addrs));
    }
    if !p.hdr_addrs.is_empty() {
        deps.insert("hdr".to_string(), str_list(p.hdr_addrs));
    }
    let embedding = !p.embed_variant.is_empty();
    if embedding && let Some(golist) = p.golist_addr {
        deps.insert(
            "golist".to_string(),
            Value::List(vec![Value::String(format!("{}|pkg", golist.format()))]),
        );
    }
    if !p.embed_file_addrs.is_empty() {
        deps.insert("embed_files".to_string(), str_list(p.embed_file_addrs));
    }
    if !p.embed_src_addrs.is_empty() {
        deps.insert("embed_src".to_string(), str_list(p.embed_src_addrs));
    }
    if let Some((sdk_group, sdk_val)) = go_sdk_dep(p.go_version) {
        deps.insert(sdk_group, sdk_val);
    }

    let import_paths: Vec<Value> = p
        .transitive_libs
        .iter()
        .map(|(ip, _)| Value::String(ip.clone()))
        .collect();

    let mut config: HashMap<String, Value> = HashMap::new();
    config.insert("p_flag".to_string(), Value::String(p.p_flag));
    config.insert("out_file".to_string(), Value::String(p.out_file.clone()));
    config.insert("goos".to_string(), Value::String(p.factors.goos.clone()));
    config.insert(
        "goarch".to_string(),
        Value::String(p.factors.goarch.clone()),
    );
    config.insert(
        "go_version".to_string(),
        Value::String(p.go_version.to_string()),
    );
    config.insert("import_paths".to_string(), Value::List(import_paths));
    config.insert("s_files".to_string(), str_list(p.s_files));
    config.insert(
        "embed_variant".to_string(),
        if embedding {
            Value::List(vec![Value::String(p.embed_variant.to_string())])
        } else {
            Value::List(vec![])
        },
    );
    config.insert("deps".to_string(), Value::Map(deps.into_iter().collect()));
    config.insert(
        "out".to_string(),
        Value::Map(HashMap::from([(
            "a".to_string(),
            Value::List(vec![Value::String(p.out_file)]),
        )])),
    );
    // NOTE: unlike the old `sh` compile, GOOS/GOARCH/CGO/GOTOOLCHAIN/GOWORK and
    // read-only/host-env handling are NOT config keys — the managed driver sets
    // env directly in run() and marks read-only via input annotations. Cache
    // correctness rides on GoCompileDef (go_version, goos, goarch, format ver).

    TargetSpec {
        addr: p.addr,
        driver: "go_compile".to_string(),
        config,
        labels: vec!["go-build".to_string()],
        transitive: Default::default(),
    }
}

#[cfg(test)]
mod driver_tests {
    use super::*;
    use crate::plugingo::factors::Factors;
    use hmodel::htpkg::PkgBuf;

    const V: &str = crate::plugingo::toolchain::DEFAULT_GO_VERSION;

    fn factors() -> Factors {
        Factors {
            goos: "linux".into(),
            goarch: "amd64".into(),
            build_tags: vec![],
        }
    }

    async fn parse_def(spec: TargetSpec) -> TargetDef {
        let ct = hcore::hasync::StdCancellationToken::new();
        GoCompileDriver::new()
            .parse(
                ParseRequest {
                    request_id: "t".to_string(),
                    target_spec: std::sync::Arc::new(spec),
                },
                &ct,
            )
            .await
            .unwrap()
            .target_def
    }

    #[tokio::test]
    async fn parse_embed_src_inputs_are_read_only_per_file_and_capture_golist() {
        let golist = Addr::new(
            PkgBuf::from("mylib"),
            "_golist".to_string(),
            Default::default(),
        );
        let spec = build_compile_spec(CompileParams {
            addr: Addr::new(
                PkgBuf::from("mylib"),
                "build_lib".to_string(),
                Default::default(),
            ),
            p_flag: "example.com/mylib".to_string(),
            out_file: "x.a".to_string(),
            factors: &factors(),
            go_version: V,
            transitive_libs: &[],
            src_addrs: &["//mylib:a.go".to_string()],
            s_files: &[],
            s_file_addrs: &[],
            hdr_addrs: &[],
            golist_addr: Some(&golist),
            embed_variant: "embed",
            embed_file_addrs: &[],
            embed_src_addrs: &["//mylib:frontend".to_string()],
        });
        let def = parse_def(spec).await;
        let es = def
            .inputs
            .iter()
            .find(|i| i.origin_id.starts_with("dep|embed_src|"))
            .expect("embed_src input present");
        assert_eq!(
            es.annotations
                .get(hdriver_support::stage::READ_ONLY_ANNOTATION)
                .map(String::as_str),
            Some("true")
        );
        assert_eq!(
            es.annotations
                .get(hdriver_support::stage::STAGE_PER_FILE_ANNOTATION)
                .map(String::as_str),
            Some("true")
        );
        let d = def.def::<GoCompileDef>();
        assert_eq!(d.embed_variant, Some(EmbedVariant::Embed));
        assert!(d.golist_origin_id.is_some(), "golist origin captured");
        assert_eq!(d.embed_src_origin_ids.len(), 1);
    }

    #[tokio::test]
    async fn parse_no_embed_leaves_variant_and_golist_unset() {
        let spec = build_compile_spec(CompileParams {
            addr: Addr::new(
                PkgBuf::from("mylib"),
                "build_lib".to_string(),
                Default::default(),
            ),
            p_flag: "example.com/mylib".to_string(),
            out_file: "x.a".to_string(),
            factors: &factors(),
            go_version: V,
            transitive_libs: &[],
            src_addrs: &["//mylib:a.go".to_string()],
            s_files: &[],
            s_file_addrs: &[],
            hdr_addrs: &[],
            golist_addr: None,
            embed_variant: "",
            embed_file_addrs: &[],
            embed_src_addrs: &[],
        });
        let def = parse_def(spec).await;
        let d = def.def::<GoCompileDef>();
        assert_eq!(d.embed_variant, None);
        assert!(d.golist_origin_id.is_none());
    }

    #[tokio::test]
    async fn parse_sdk_input_is_read_only() {
        let spec = build_compile_spec(CompileParams {
            addr: Addr::new(
                PkgBuf::from("mylib"),
                "build_lib".to_string(),
                Default::default(),
            ),
            p_flag: "example.com/mylib".to_string(),
            out_file: "x.a".to_string(),
            factors: &factors(),
            go_version: V,
            transitive_libs: &[],
            src_addrs: &["//mylib:a.go".to_string()],
            s_files: &[],
            s_file_addrs: &[],
            hdr_addrs: &[],
            golist_addr: None,
            embed_variant: "",
            embed_file_addrs: &[],
            embed_src_addrs: &[],
        });
        let def = parse_def(spec).await;
        let prefix = format!("dep|{}|", crate::plugingo::addr_util::GO_SDK_DEP_GROUP);
        let sdk = def
            .inputs
            .iter()
            .find(|i| i.origin_id.starts_with(&prefix))
            .expect("sdk input present");
        assert_eq!(
            sdk.annotations
                .get(hdriver_support::stage::READ_ONLY_ANNOTATION)
                .map(String::as_str),
            Some("true")
        );
    }
}

/// Shared helpers for asserting on `go_compile` specs from the builders' tests.
#[cfg(test)]
pub(crate) mod test_support {
    use hcore::htvalue::Value;
    use hplugin::provider::TargetSpec;
    use std::collections::HashMap;

    pub(crate) fn cfg_str(spec: &TargetSpec, k: &str) -> String {
        match spec
            .config
            .get(k)
            .unwrap_or_else(|| panic!("missing config {k}"))
        {
            Value::String(s) => s.clone(),
            v => panic!("{k} not a string: {v:?}"),
        }
    }

    pub(crate) fn cfg_list(spec: &TargetSpec, k: &str) -> Vec<String> {
        match spec
            .config
            .get(k)
            .unwrap_or_else(|| panic!("missing config {k}"))
        {
            Value::List(l) => l
                .iter()
                .map(|x| match x {
                    Value::String(s) => s.clone(),
                    _ => panic!("{k} entry not a string"),
                })
                .collect(),
            v => panic!("{k} not a list: {v:?}"),
        }
    }

    pub(crate) fn deps_map(spec: &TargetSpec) -> HashMap<String, Vec<String>> {
        match spec.config.get("deps").unwrap() {
            Value::Map(m) => m
                .iter()
                .map(|(k, v)| {
                    let list = match v {
                        Value::List(l) => l
                            .iter()
                            .map(|x| match x {
                                Value::String(s) => s.clone(),
                                _ => panic!("dep entry not a string"),
                            })
                            .collect(),
                        _ => panic!("dep group not a list"),
                    };
                    (k.clone(), list)
                })
                .collect(),
            _ => panic!("deps not a map"),
        }
    }
}
