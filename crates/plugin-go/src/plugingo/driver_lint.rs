/// Output filenames (package-relative). Kept in sync between [`build_lint_spec`]
/// (the declared outputs) and [`GoLintDriver::run`] (where they are written).
const LINT_FACTS: &str = "lint.facts";
const LINT_REPORT: &str = "lint-report.json";

/// Dep group carrying the `heph-govet` unitchecker binary.
const GOVET_TOOL_GROUP: &str = "govet_tool";

/// Dep group prefix for a dependency's serialized facts (parallel to the
/// `lib_*` archive groups). `"github.com/foo/bar"` → `"facts_github_com_foo_bar"`.
fn import_path_to_facts_group(import_path: &str) -> String {
    let sanitized: String = import_path
        .chars()
        .map(|c| {
            if c.is_alphanumeric() || c == '_' {
                c
            } else {
                '_'
            }
        })
        .collect();
    format!("facts_{sanitized}")
}

