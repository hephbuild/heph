    #[test]
    fn gate_empty_report_has_no_findings() {
        assert!(report_findings(b"{}").unwrap().is_empty());
        assert!(report_findings(b"   \n ").unwrap().is_empty());
        assert!(report_findings(b"").unwrap().is_empty());
    }

    #[test]
    fn gate_extracts_findings_with_posn_and_message() {
        let json = br#"{
            "example.com/mylib": {
                "printf": [
                    {"posn": "a.go:10:2", "message": "Printf format %d has arg s of wrong type string"}
                ],
                "lostcancel": [
                    {"posn": "a.go:20:2", "message": "the cancel function is not used"}
                ]
            }
        }"#;
        let f = report_findings(json).unwrap();
        assert_eq!(f.len(), 2);
        assert!(f.iter().any(|s| s.contains("a.go:10:2") && s.contains("printf")));
        assert!(f.iter().any(|s| s.contains("lostcancel")));
    }

    #[test]
    fn gate_empty_analyzer_arrays_yield_no_findings() {
        let json = br#"{"example.com/mylib": {"printf": []}}"#;
        assert!(report_findings(json).unwrap().is_empty());
    }

    #[test]
    fn gate_spec_driver_and_report_dep() {
        let analyze = Addr::new(
            PkgBuf::from("mylib"),
            "_lint".to_string(),
            Default::default(),
        );
        let s = build_lint_gate_spec(addr("lint"), &analyze);
        assert_eq!(s.driver, "go_lint_gate");
        let deps = match s.config.get("deps").unwrap() {
            Value::Map(m) => m,
            _ => panic!("deps not a map"),
        };
        let report = match deps.get("report").unwrap() {
            Value::List(l) => l,
            _ => panic!("report not a list"),
        };
        match &report[0] {
            Value::String(s) => assert!(
                s.contains(":_lint") && s.ends_with("|report"),
                "gate must depend on the analyze target's report output: {s}"
            ),
            _ => panic!("not a string"),
        }
    }
