//! `go_lint` — per-package go/analysis driver producing serialized facts.
//!
//! Mirrors [`crate::plugingo::driver_compile`] but instead of `go tool compile`
//! it runs the hermetic `heph-govet` binary (an `x/tools` unitchecker, built as
//! an ordinary Go target and staged read-only). It analyzes exactly ONE package:
//!
//!   - dep type info  → export data `.a` archives, via the `lib_*` dep groups
//!     (identical wiring to `go_compile`'s importcfg);
//!   - dep facts      → each dep's `lint.facts`, via the `facts_*` dep groups;
//!   - own facts out  → `lint.facts` (the `facts` output group), consumed by
//!     dependents;
//!   - diagnostics    → `lint-report.json` (the `report` output group), gated by
//!     a separate cheap target so facts are produced/cached even when a package
//!     currently has findings.
//!
//! This is the nogo model. Per-package caching falls out of the engine's
//! content-hashing of inputs: a package re-lints only when its own sources, a
//! dep's archive, or a dep's facts change. Interprocedural analyzers (printf
//! wrappers, lostcancel, …) keep full fidelity because facts cross the boundary —
//! which export-data-only linting cannot do.

